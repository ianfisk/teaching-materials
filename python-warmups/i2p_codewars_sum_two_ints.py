def sum_two_smallest_numbers_functional(numbers):
	return sum(reduce(
		lambda mins, num:
				(num, mins[0]) if num < mins[0]
				else (mins[0], num) if num < mins[1]
				else mins,
		numbers,
		(math.inf, math.inf)))

def sum_two_smallest_numbers_loop(numbers):
	min1 = math.inf
	min2 = math.inf
	for num in numbers:
			if num < min1:
					min2 = min1
					min1 = num
			elif num < min2:
					min2 = num

	return min1 + min2

def sum_two_smallest_numbers_sort(numbers):
	sortedNumbers = numbers.copy()
	sortedNumbers.sort()
	min1, min2, *rest = sortedNumbers

	return min1 + min2

